#include <stdio.h>
int main()
{
	int a = 7;
	int total = 21;
	
	printf("value of the total = %d\n",total+=a);
	
    printf("value of the total = %d\n",total-=a);
    
    	printf("value of the total = %d\n",total*=a);
    	
    		printf("value of the total = %d\n",total/=a);
    		
    			printf("value of the total = %d\n",total%=a);
    			
    			return 0;
}
